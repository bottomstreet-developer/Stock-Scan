        ? null
        : marginItems.reduce(
            (a, b) =>
                ((a.sellPrice - a.costPrice) / a.sellPrice) >
                        ((b.sellPrice - b.costPrice) / b.sellPrice)
                    ? a
                    : b,
          );
    final bestMargin = bestItem == null
        ? 0.0
        : (bestItem.sellPrice - bestItem.costPrice) / bestItem.sellPrice * 100;
