                    _summaryItem(
                      'Inventory Value',
                      '$currencySymbol${totalValue.toStringAsFixed(2)}',
                    ),
                    _summaryItem(
                      'Potential Revenue',
                      '$currencySymbol${totalRevenue.toStringAsFixed(2)}',
                    ),
                    _summaryItem(
                      'Potential Profit',
                      '$currencySymbol${totalProfit.toStringAsFixed(2)}',
                    ),
