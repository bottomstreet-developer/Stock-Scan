class DashboardScreen extends StatefulWidget {
  const DashboardScreen({super.key});

  @override
  State<DashboardScreen> createState() => _DashboardScreenState();
}

class _DashboardScreenState extends State<DashboardScreen> {
  bool _loading = true;
  List<Item> _items = [];
  List<Item> _lowStock = [];
  String? _errorMessage;

  @override
  void initState() {
    super.initState();
    PurchaseService.instance.addListener(_onProChanged);
    inventoryChangeNotifier.addListener(_onInventoryChanged);
    _loadData();
  }

  @override
  void dispose() {
    PurchaseService.instance.removeListener(_onProChanged);
    inventoryChangeNotifier.removeListener(_onInventoryChanged);
    super.dispose();
  }

  void _onProChanged() => setState(() {});

  void _onInventoryChanged() => _loadData();

  Future<void> _loadData() async {
    setState(() => _loading = true);
    try {
      final items = await DatabaseService.instance.getAllItems();
      final lowStock = await DatabaseService.instance.getLowStockItems();
      if (!mounted) return;
      setState(() {
        _items = items;
        _lowStock = lowStock;
        _errorMessage = null;
      });
    } catch (_) {
      if (!mounted) return;
      setState(() => _errorMessage = 'Unable to load dashboard.');
    } finally {
      if (mounted) setState(() => _loading = false);
    }
  }

  Future<void> _exportPDF() async {
    final currencySymbol = PrefsService.instance.currency;
    final dateFormat = DateFormat('MMM d, yyyy');
    final uniqueItems = _items.fold<List<Item>>([], (list, item) {
      if (!list.any((e) => e.id == item.id)) list.add(item);
      return list;
    });
    final totalItems = _items.length;
    final totalValue = _items.fold<double>(0, (sum, item) => sum + item.totalValue);
    final totalRevenue = _items.fold<double>(0, (sum, item) => sum + item.totalRevenue);
    final totalProfit = totalRevenue - totalValue;
    final lowStock = uniqueItems.where((item) => item.isLowStock).toList();

    final pdf = pw.Document();
    pdf.addPage(
      pw.MultiPage(
        pageFormat: PdfPageFormat.a4,
        margin: const pw.EdgeInsets.all(40),
        footer: (context) => pw.Padding(
          padding: const pw.EdgeInsets.only(top: 8),
          child: pw.Row(
            mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
            children: [
              pw.Text(
                'Generated by StockSnap',
                style: pw.TextStyle(
                  fontSize: 8,
                  color: PdfColors.grey500,
                ),
              ),
              pw.Text(
                'Page ${context.pageNumber} of ${context.pagesCount}',
                style: pw.TextStyle(
                  fontSize: 8,
                  color: PdfColors.grey500,
                ),
              ),
            ],
          ),
        ),
        build: (context) => [
          pw.Row(
            mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
            children: [
              pw.Text(
                'StockSnap',
                style: pw.TextStyle(
                  fontSize: 24,
                  fontWeight: pw.FontWeight.bold,
                ),
              ),
              pw.Text(
                dateFormat.format(DateTime.now()),
                style: const pw.TextStyle(
                  fontSize: 11,
                  color: PdfColors.grey600,
                ),
              ),
            ],
          ),
          pw.SizedBox(height: 24),
          pw.Divider(color: PdfColors.grey300, thickness: 0.5),
